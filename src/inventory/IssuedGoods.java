    private void txtgoodcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtgoodcodeKeyPressed
        // TODO add your handling code here:
        
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                 String name = txtgoodcode.getText();

                try {
                     Class.forName("com.mysql.cj.jdbc.Driver");
                     con1 = DriverManager.getConnection("jdbc:mysql://localhost/trackwise", "root", "");

                    insert = con1.prepareStatement("select * from goods where up_code=? ");
                    insert.setString(1, name);
                    rs = insert.executeQuery();

                     if (rs.next() == false) {
                         JOptionPane.showMessageDialog(this, "UPCode not found");
                    } else {
                        String goodname = rs.getString("goodName");
                        String price = rs.getString("retail_price");

                        txtgoodname.setText(goodname.trim());
                        txtprice.setText(price.trim());
                    }
                } catch (ClassNotFoundException | SQLException ex) {
                    // ...
                }
            }
              
    }//GEN-LAST:event_txtgoodcodeKeyPressed

