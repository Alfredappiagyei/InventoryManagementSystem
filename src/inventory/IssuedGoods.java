 private void issue()
{
    String name = txtgoodcode.getText();

    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        con1 = DriverManager.getConnection("jdbc:mysql://localhost/trackwise", "root", "");
        insert = con1.prepareStatement("select * from goods where up_code=? ");
        insert.setString(1, name);
        rs = insert.executeQuery();

        while (rs.next())
        {

            int currentqty;

            currentqty = rs.getInt("qty");

            int price = Integer.parseInt(txtprice.getText());
            int qtynew = Integer.parseInt(txtqty.getText());

            int tot = price * qtynew;

            if (qtynew >= currentqty)
            {
                JOptionPane.showMessageDialog(this, "Available Goods" + " = " + currentqty);
                JOptionPane.showMessageDialog(this, "Quantity is not enough");
            }
            else
            {
                if (jTable1.getRowCount() >= 0)
                {
                    model = (DefaultTableModel) jTable1.getModel();
                    model.addRow(new Object[]
                    {
                        txtgoodcode.getText(),
                        txtgoodname.getText(),
                        txtprice.getText(),
                        txtqty.getText(),
                        tot,
                    });


                    int sum = 0;
                    for (int i = 0; i < jTable1.getRowCount(); i++)
                    {

                        if (jTable1.getValueAt(i, 4) != null)
                        {
                            sum = sum + Integer.parseInt(jTable1.getValueAt(i, 4).toString());
                        }
                    }

                    txtsub.setText(Integer.toString(sum));




                    txtgoodcode.setText("");
                    txtgoodname.setText("");
                    txtprice.setText("");
                    txtqty.setText("");
                    txtgoodcode.requestFocus();

                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Table is empty");
                }
            }

        }



    } catch (ClassNotFoundException ex) {
        Logger.getLogger(IssuedGoods.class.getName()).log(Level.SEVERE, null, ex);
    } catch (SQLException ex) {
        Logger.getLogger(IssuedGoods.class.getName()).log(Level.SEVERE, null, ex);
    }

}

    
    
    
 
    
